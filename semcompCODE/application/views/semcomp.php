						A SEMCOMP (Semana de Computação da UFBA) nasceu para reaproximar e movimentar a comunidade de computação da Universidade Federal da Bahia. Possuindo como seu subtema a frase ‘’connecting the dots” o evento propõe a ligação dos pontos entre a academia e o mercado, bem como os pontos sociais entre professores, alunos e a comunidade de TI local.<br />
						Em sua primeira edição irá propor palestras com figuras importantes no cenário nacional da inovação, empreendedorismo e computação, além da promoção de minicursos e atividades que promovam a capacitação técnica de seus participantes quanto as mais novas tecnologias do mercado.</p>

